    private void jb_loginMain1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jb_loginMain1ActionPerformed
        String UserName = jText_Username.getText();
        String Password = jText_UserPassword.getText();
        String TempUserID =DB.VerifyUser(UserName,Password);
        User_ID = TempUserID;
        if (!"Wrong User".equals(TempUserID)) {
            System.out.println(DB.GetField("User","ID",TempUserID,"Type"));
            if ("Administrator".equals(DB.GetField("User","ID",TempUserID,"Type"))){
                Administrator.setVisible(true);
                Administrator.pack();
                Administrator.setModalExclusionType(Dialog.ModalExclusionType.NO_EXCLUDE);
            }else if ("Student".equals(DB.GetField("User","ID",TempUserID,"Type"))) {
                studentID = DB.GetField("User", "ID", TempUserID, "ID");
                fillStudentInfo(studentID);
                this.StudentJFrame.pack();
                this.StudentJFrame.setVisible(true);
            }else if("Teacher".equals(DB.GetField("User","ID",TempUserID,"Type"))){
                TeacherNametxt.setText(DB.GetField("Teacher", "TeacherID", TempUserID, "Name"));
                TeacherIDtxt.setText(TempUserID);
                TeacherLastNametxt.setText(DB.GetField("Teacher","TeacherID",TempUserID,"LastName"));
                TeacherPhonetxt.setText(DB.GetField("Teacher","TeacherID",TempUserID,"Phone"));
                TeacherAddresstxt.setText(DB.GetField("Teacher","TeacherID",TempUserID,"Address"));
                TeacherLeveltxt.setText(DB.GetField("Teacher","TeacherID",TempUserID,"Level"));
                refreshTeacherCourses(TempUserID);
                if(DB.GetField("Car","TeacherID",TempUserID,"TeacherID")!=null)
                {
                    TeacherCartxt.setText(DB.GetField("Car","TeacherID",TempUserID,"CarID"));
                    TeacherMileagetxt.setText(DB.GetField("Car","TeacherID",TempUserID,"CarMileage"));
                }
                else
                {
                    TeacherCartxt.setText("None");
                    TeacherMileagetxt.setText("None");
                }
                this.Teacher.pack();
                this.Teacher.setVisible(true);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Incorrect Password or Username");
        }
            
    }//GEN-LAST:event_jb_loginMain1ActionPerformed

